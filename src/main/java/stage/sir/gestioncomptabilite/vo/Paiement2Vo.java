package stage.sir.gestioncomptabilite.vo;

public class Paiement2Vo {
    public String ref;
    public String societeis;
    public String anneeis;
    public String societetva;
    public String anneetva;
    public String mois;
    public String trim;

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getSocieteis() {
        return societeis;
    }

    public void setSocieteis(String societeis) {
        this.societeis = societeis;
    }

    public String getAnneeis() {
        return anneeis;
    }

    public void setAnneeis(String anneeis) {
        this.anneeis = anneeis;
    }

    public String getSocietetva() {
        return societetva;
    }

    public void setSocietetva(String societetva) {
        this.societetva = societetva;
    }

    public String getAnneetva() {
        return anneetva;
    }

    public void setAnneetva(String anneetva) {
        this.anneetva = anneetva;
    }

    public String getMois() {
        return mois;
    }

    public void setMois(String mois) {
        this.mois = mois;
    }

    public String getTrim() {
        return trim;
    }

    public void setTrim(String trim) {
        this.trim = trim;
    }
}
